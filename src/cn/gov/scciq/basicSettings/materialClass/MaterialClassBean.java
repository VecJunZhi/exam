package cn.gov.scciq.basicSettings.materialClass;

public class MaterialClassBean {
	private String materialClassID;
	private String classCode;
	private String className;
	private String ifSet;
	private String materialSubclassID;
	private String subclassCode;
	private String subclassName;
	private String materialID;
	private String materialCode;
	private String materialName;
	private String materialSubID;
	private String materialCodeSub;
	public MaterialClassBean() {
		super();
	}
	public String getMaterialClassID() {
		return materialClassID;
	}
	public void setMaterialClassID(String materialClassID) {
		this.materialClassID = materialClassID;
	}
	public String getClassCode() {
		return classCode;
	}
	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getIfSet() {
		return ifSet;
	}
	public void setIfSet(String ifSet) {
		this.ifSet = ifSet;
	}
	public String getMaterialSubclassID() {
		return materialSubclassID;
	}
	public void setMaterialSubclassID(String materialSubclassID) {
		this.materialSubclassID = materialSubclassID;
	}
	public String getSubclassCode() {
		return subclassCode;
	}
	public void setSubclassCode(String subclassCode) {
		this.subclassCode = subclassCode;
	}
	public String getSubclassName() {
		return subclassName;
	}
	public void setSubclassName(String subclassName) {
		this.subclassName = subclassName;
	}
	public String getMaterialID() {
		return materialID;
	}
	public void setMaterialID(String materialID) {
		this.materialID = materialID;
	}
	public String getMaterialCode() {
		return materialCode;
	}
	public void setMaterialCode(String materialCode) {
		this.materialCode = materialCode;
	}
	public String getMaterialName() {
		return materialName;
	}
	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
	public String getMaterialSubID() {
		return materialSubID;
	}
	public void setMaterialSubID(String materialSubID) {
		this.materialSubID = materialSubID;
	}
	public String getMaterialCodeSub() {
		return materialCodeSub;
	}
	public void setMaterialCodeSub(String materialCodeSub) {
		this.materialCodeSub = materialCodeSub;
	}
	
}
